console.log("loopa 1-5");
for (let i = 1; i <= 5; i++) {
  console.log(i);
}

console.log("loopa sem squarar");
for (let i = 1; i <= 5; i++) {
  let squared = i * i;
  console.log(squared);
}

console.log("loopa 10-0");
for (let i = 10; i > 0; i--) {
  console.log(i);
}

console.log("loopa frá 0-10 með 2 á milli");
for (let i = 0; i <= 10; i += 2) {
  console.log(i);
}
